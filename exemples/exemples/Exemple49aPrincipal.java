public class Exemple49aPrincipal {
    public static int maxim(int a, int b){
        if(a>=b) {
            return a;
        }            
        return b;
    }
    
    public static int minim(int a, int b){
        if(a<=b) {
            return a;
        }            
        return b;
    }
}
